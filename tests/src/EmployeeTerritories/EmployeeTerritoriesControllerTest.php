<?php

declare(strict_types=1);

namespace Northwind\Tests\EmployeeTerritories;

use PHPUnit\Framework\TestCase;
use Northwind\EmployeeTerritories\{ EmployeeTerritoriesDto, EmployeeTerritoriesModel, EmployeeTerritoriesController };

class EmployeeTerritoriesControllerTest extends TestCase
{
    private array $input;
    private EmployeeTerritoriesDto $dto;
    private EmployeeTerritoriesModel $model;
    private $service;
    private $request;
    private $stream;
    private EmployeeTerritoriesController $controller;

    protected function setUp(): void
    {
        $this->input = [
            "employee_territories_id" => 7486,
            "employee_id" => 9731,
            "territory_id" => "Social similar recent message.",
        ];
        $this->dto = new EmployeeTerritoriesDto($this->input);
        $this->model = new EmployeeTerritoriesModel($this->dto);
        $this->service = $this->createMock("Northwind\EmployeeTerritories\IEmployeeTerritoriesService");
        $this->request = $this->createMock("Psr\Http\Message\ServerRequestInterface");
        $this->stream = $this->createMock("Psr\Http\Message\StreamInterface");
        $this->controller = new EmployeeTerritoriesController(
            $this->service
        );

        $this->stream->method("getContents")
            ->willReturn("[]");

        $this->request->method("getBody")
            ->willReturn($this->stream);

        $this->request->method("getParsedBody")
            ->willReturn($this->input);
    }

    protected function tearDown(): void
    {
        unset($this->input);
        unset($this->dto);
        unset($this->model);
        unset($this->service);
        unset($this->request);
        unset($this->stream);
        unset($this->controller);
    }

    public function testInsert_ReturnsResponse(): void
    {
        $id = 5806;
        $expected = ["result" => $id];
        $args = [];

        $this->service->expects($this->once())
            ->method("createModel")
            ->with($this->request->getParsedBody())
            ->willReturn($this->model);
        $this->service->expects($this->once())
            ->method("insert")
            ->with($this->model)
            ->willReturn($id);

        $actual = $this->controller->insert($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testUpdate_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["employee_territories_id" => 0];

        $actual = $this->controller->update($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testUpdate_ReturnsResponse(): void
    {
        $id = 6783;
        $expected = ["result" => $id];
        $args = ["employee_territories_id" => 8467];

        $this->service->expects($this->once())
            ->method("createModel")
            ->with($this->request->getParsedBody())
            ->willReturn($this->model);
        $this->service->expects($this->once())
            ->method("update")
            ->with($this->model)
            ->willReturn($id);

        $actual = $this->controller->update($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGet_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["employee_territories_id" => 0];

        $actual = $this->controller->get($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGet_ReturnsResponse(): void
    {
        $expected = ["result" => $this->model->jsonSerialize()];
        $args = ["employee_territories_id" => 7251];

        $this->service->expects($this->once())
            ->method("get")
            ->with($args["employee_territories_id"])
            ->willReturn($this->model);

        $actual = $this->controller->get($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGetAll_ReturnsResponse(): void
    {
        $expected = ["result" => [$this->model->jsonSerialize()]];
        $args = [];

        $this->service->expects($this->once())
            ->method("getAll")
            ->willReturn([$this->model]);

        $actual = $this->controller->getAll($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testDelete_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["employee_territories_id" => 0];

        $actual = $this->controller->delete($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testDelete_ReturnsResponse(): void
    {
        $id = 7187;
        $expected = ["result" => $id];
        $args = ["employee_territories_id" => 2879];

        $this->service->expects($this->once())
            ->method("delete")
            ->with($args["employee_territories_id"])
            ->willReturn($id);

        $actual = $this->controller->delete($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }
}
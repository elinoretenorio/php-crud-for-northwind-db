<?php

declare(strict_types=1);

namespace Northwind\Tests\CustomerDemographics;

use PHPUnit\Framework\TestCase;
use Northwind\CustomerDemographics\{ CustomerDemographicsDto, CustomerDemographicsModel, CustomerDemographicsController };

class CustomerDemographicsControllerTest extends TestCase
{
    private array $input;
    private CustomerDemographicsDto $dto;
    private CustomerDemographicsModel $model;
    private $service;
    private $request;
    private $stream;
    private CustomerDemographicsController $controller;

    protected function setUp(): void
    {
        $this->input = [
            "customer_demographics_id" => 3902,
            "customer_type_id" => 3608,
            "customer_desc" => "Sign remain grow technology product lawyer detail she. Make history democratic party gun lose.",
        ];
        $this->dto = new CustomerDemographicsDto($this->input);
        $this->model = new CustomerDemographicsModel($this->dto);
        $this->service = $this->createMock("Northwind\CustomerDemographics\ICustomerDemographicsService");
        $this->request = $this->createMock("Psr\Http\Message\ServerRequestInterface");
        $this->stream = $this->createMock("Psr\Http\Message\StreamInterface");
        $this->controller = new CustomerDemographicsController(
            $this->service
        );

        $this->stream->method("getContents")
            ->willReturn("[]");

        $this->request->method("getBody")
            ->willReturn($this->stream);

        $this->request->method("getParsedBody")
            ->willReturn($this->input);
    }

    protected function tearDown(): void
    {
        unset($this->input);
        unset($this->dto);
        unset($this->model);
        unset($this->service);
        unset($this->request);
        unset($this->stream);
        unset($this->controller);
    }

    public function testInsert_ReturnsResponse(): void
    {
        $id = 2224;
        $expected = ["result" => $id];
        $args = [];

        $this->service->expects($this->once())
            ->method("createModel")
            ->with($this->request->getParsedBody())
            ->willReturn($this->model);
        $this->service->expects($this->once())
            ->method("insert")
            ->with($this->model)
            ->willReturn($id);

        $actual = $this->controller->insert($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testUpdate_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["customer_demographics_id" => 0];

        $actual = $this->controller->update($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testUpdate_ReturnsResponse(): void
    {
        $id = 4527;
        $expected = ["result" => $id];
        $args = ["customer_demographics_id" => 9089];

        $this->service->expects($this->once())
            ->method("createModel")
            ->with($this->request->getParsedBody())
            ->willReturn($this->model);
        $this->service->expects($this->once())
            ->method("update")
            ->with($this->model)
            ->willReturn($id);

        $actual = $this->controller->update($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGet_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["customer_demographics_id" => 0];

        $actual = $this->controller->get($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGet_ReturnsResponse(): void
    {
        $expected = ["result" => $this->model->jsonSerialize()];
        $args = ["customer_demographics_id" => 238];

        $this->service->expects($this->once())
            ->method("get")
            ->with($args["customer_demographics_id"])
            ->willReturn($this->model);

        $actual = $this->controller->get($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGetAll_ReturnsResponse(): void
    {
        $expected = ["result" => [$this->model->jsonSerialize()]];
        $args = [];

        $this->service->expects($this->once())
            ->method("getAll")
            ->willReturn([$this->model]);

        $actual = $this->controller->getAll($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testDelete_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["customer_demographics_id" => 0];

        $actual = $this->controller->delete($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testDelete_ReturnsResponse(): void
    {
        $id = 9392;
        $expected = ["result" => $id];
        $args = ["customer_demographics_id" => 1167];

        $this->service->expects($this->once())
            ->method("delete")
            ->with($args["customer_demographics_id"])
            ->willReturn($id);

        $actual = $this->controller->delete($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }
}